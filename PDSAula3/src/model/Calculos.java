package model;

public class Calculos {

	public Integer CalcAdição (int PrimeiroNumero, int SegundoNumero) {	
		int resultado;
		resultado = PrimeiroNumero + SegundoNumero;		
		return resultado;
	}
	
	public Integer CalcSubtracao (int PrimeiroNumero, int SegundoNumero) {
		int resultado;
		resultado = PrimeiroNumero - SegundoNumero;
		return resultado;
	}
	
	public Integer CalcMultiplicacao(int PrimeiroNumero, int SegundoNumero) {
		int resultado;
		resultado = PrimeiroNumero * SegundoNumero;
		return resultado;
		
	}
	
	
}
