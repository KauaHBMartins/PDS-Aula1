package model;

public class Pessoa {
	
	private String nomePessoa;
	private int cpfPessoa;
	private int telefonePessoa;
	public String getNomePessoa() {
		return nomePessoa;
	}
	public void setNomePessoa(String nomePessoa) {
		this.nomePessoa = nomePessoa;
	}
	public int getCpfPessoa() {
		return cpfPessoa;
	}
	public void setCpfPessoa(int cpfPessoa) {
		this.cpfPessoa = cpfPessoa;
	}
	public int getTelefonePessoa() {
		return telefonePessoa;
	}
	public void setTelefonePessoa(int telefonePessoa) {
		this.telefonePessoa = telefonePessoa;
	}
	public int getIdadePessoa() {
		return idadePessoa;
	}
	public void setIdadePessoa(int idadePessoa) {
		this.idadePessoa = idadePessoa;
	}
	public float getPesoPessoa() {
		return pesoPessoa;
	}
	public void setPesoPessoa(float pesoPessoa) {
		this.pesoPessoa = pesoPessoa;
	}
	public float getAlturaPessoa() {
		return alturaPessoa;
	}
	public void setAlturaPessoa(float alturaPessoa) {
		this.alturaPessoa = alturaPessoa;
	}
	private int idadePessoa;
	private float pesoPessoa;
	private float alturaPessoa;
	
	
	
	
	

}
